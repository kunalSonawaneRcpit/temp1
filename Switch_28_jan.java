public class Switch_28_jan {
    public static void main(String[] args) {
        int num=100;
        switch(num)
        {
            case 50 :
                System.out.println("Case 50");
                break;
            case 100 :
                System.out.println("Case 100");
                break;
            case 200 :
                System.out.println("Case 200");
                break;
            default :
                System.out.println("Default");
                break;
        }
    }
}
